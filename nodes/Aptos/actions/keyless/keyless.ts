/**
 * Keyless Resource Actions
 */

import type { IExecuteFunctions, INodeProperties, IDataObject } from 'n8n-workflow';
import { AptosClientWrapper } from '../../transport/aptosClient';

export const keylessOperations: INodeProperties[] = [
  {
    displayName: 'Operation',
    name: 'operation',
    type: 'options',
    noDataExpression: true,
    displayOptions: { show: { resource: ['keyless'] } },
    options: [
      { name: 'Get', value: 'get', action: 'Get keyless' },
      { name: 'List', value: 'list', action: 'List keylesss' },
    ],
    default: 'get',
  },
];

export const keylessFields: INodeProperties[] = [
  {
    displayName: 'Address',
    name: 'address',
    type: 'string',
    required: true,
    default: '',
    displayOptions: { show: { resource: ['keyless'] } },
  },
];

export async function executeKeylessOperation(
  execFns: IExecuteFunctions,
  client: AptosClientWrapper,
  operation: string,
  index: number,
): Promise<IDataObject> {
  const address = execFns.getNodeParameter('address', index) as string;
  return { resource: 'keyless', operation, address, note: 'Operation placeholder' };
}
